public class Buttons {

	private int numberOfButtons;


	//constructor
	public Buttons(int numberOfButtons) {
		this.numberOfButtons = numberOfButtons;
	}

	//get Method
	public int getNumberOfButtons() {

		return numberOfButtons;

	}


	//set Method
	public void setNumberOfButtons(int newButtonAmount) {

		numberOfButtons = newButtonAmount;

	}

}

